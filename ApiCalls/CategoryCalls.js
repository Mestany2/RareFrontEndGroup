const getAllCategories = () => new Promise((resolve, reject) => {
  fetch('https://localhost:7100/api/Categories', {
    method: 'GET',
    headers: {
      'Content-Type': 'application/json',
    },
  })
    .then(async (res) => {
      let data;
      if (res.ok) {
        data = await res.json();
        resolve(data);
      }
    })
    .catch(reject);
});

export { getAllCategories };
